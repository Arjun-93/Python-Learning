# Variables and Datatypes 

# Here are some examples of variables and datatypes
# _int is a container for integer data type
_int = 1
_float = 1.0
_String = "Hello World"
_char = 'H'
_Boolean = True
_complex = 9 + 7j
# for checking the type of variable use type() function
print(_int, type(_int))
print(_float, type(_float))
print(_String,  type(_String))
print(_char, type(_char))
print(_Boolean, type(_Boolean))
print(_complex, type(_complex))

# in python we can change the type of variable and it is called dynamic typing
# for example we can change the type of _int variable from integer to string and vice versa 
# In python programming every variable is an object and every object has a type and a value.

