
# Here are some examples of Arithmetic Operators

print(59 + 3) # Addition
print(59 - 3) # Subtraction
print(59 * 3) # Multiplication
print(59 / 3) # Division
print(59 % 3) # Modulus
print(59 ** 3) # Exponentiation
print(59 // 3) # Floor Division
print(59 + 3 * 2) # Order of Operations
print((59 + 3) * 2) # Order of Operations
print(59 + 3 * 2 ** 2) # Order of Operations
print((59 + 3) * 2 ** 2) # Order of Operations
print(59 + 3 * 2 ** 2 // 2) # Order of Operations
print((59 + 3) * 2 ** 2 // 2) # Order of Operations